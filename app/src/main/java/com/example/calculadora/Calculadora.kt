package com.example.calculadora

class Calculadora
{

    fun somar(n1:Int, n2:Int): Int{
        val resultado = n1 + n2
        return (resultado )
    }



    fun subtrair(n1:Int, n2:Int): Int{
        val resultado = n1 - n2
        return (resultado )
    }

    fun multplicacao(n1:Int, n2:Int): Int{
        val resultado = n1 * n2
        return (resultado )
    }

    fun divisao(n1:Int, n2:Int): Int{
        val resultado = n1 / n2
        return (resultado )
    }


}